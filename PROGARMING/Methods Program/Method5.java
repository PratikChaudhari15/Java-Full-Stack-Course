class Method5
{
    public static void main(String[] Args)
    {
        add(10, 50);   // Call add() method with 10 and 50
        add(286, 76);  // Call add() method with 286 and 76
        add(89, 22);   // Call add() method with 89 and 22
    }

    public static void add(int a, int b)
    {
        int res = a + b;  // Add the two numbers
        System.out.println(res);  // Print the result
    }
}
